import tkinter as tk


def mostrar_numeros(numeros):
    ventana = tk.Tk()
    ventana.geometry("400x300")  # Tamano de la ventana

    # Crear un widget de etiqueta para mostrar el numero en grande
    etiqueta_numero = tk.Label(ventana, font=("Arial", 100))

    # Funcion para cambiar el numero mostrado en la etiqueta
    def cambiar_numero():
        if len(numeros) > 0:
            numero_actual = numeros.pop(0)
            etiqueta_numero.config(text=numero_actual)
        else:
            etiqueta_numero.config(text="No hay mas numeros")
        # Cambiar el numero al presionar Enter
        ventana.bind("<Return>", lambda event: cambiar_numero())

    cambiar_numero()

    # Anadir la etiqueta a la ventana
    etiqueta_numero.pack(expand=True)

    ventana.mainloop()


